def resolve_acceptance_generated_report_path(root: Path, report_path: str | Path) -> Path:
    root_abs = Path(os.path.abspath(root))
    candidate = Path(report_path)
    if not candidate.is_absolute():
        candidate = root_abs / candidate
    target = Path(os.path.abspath(candidate))
    reports_root = Path(os.path.abspath(root_abs / "reports"))
    try:
        target.relative_to(root_abs)
    except ValueError as exc:
        raise ValueError("report path must stay inside the memory root") from exc
    try:
        target.relative_to(reports_root)
    except ValueError as exc:
        raise ValueError("report path must stay under reports/") from exc
    reject_acceptance_packet_symlink_components(root_abs, target, "report path")
    return target


def reject_acceptance_packet_symlink_components(root_abs: Path, target: Path, label: str) -> None:
    current = root_abs
    for part in target.relative_to(root_abs).parts:
        current = current / part
        if current.is_symlink():
            raise ValueError(f"{label} must not contain symlinks")


def resolve_acceptance_packet_archive_dir(
    root: Path,
    archive_dir: str | Path = DEFAULT_ACCEPTANCE_PACKET_ARCHIVE_DIR,
) -> Path:
    root_abs = Path(os.path.abspath(root))
    candidate = Path(archive_dir)
    if not candidate.is_absolute():
        candidate = root_abs / candidate
    target_dir = Path(os.path.abspath(candidate))
    allowed_dir = Path(os.path.abspath(root_abs / DEFAULT_ACCEPTANCE_PACKET_ARCHIVE_DIR))
    try:
        target_dir.relative_to(root_abs)
    except ValueError as exc:
        raise ValueError("archive dir must stay inside the memory root") from exc
    try:
        target_dir.relative_to(allowed_dir)
    except ValueError as exc:
        raise ValueError("archive dir must stay under reports/manual-acceptance-packets") from exc
    reject_acceptance_packet_symlink_components(root_abs, target_dir, "archive dir")
    return target_dir


def acceptance_packet_archive_path(
    root: Path,
    archive_dir: str | Path = DEFAULT_ACCEPTANCE_PACKET_ARCHIVE_DIR,
    *,
    now: datetime | None = None,
) -> Path:
    target_dir = resolve_acceptance_packet_archive_dir(root, archive_dir)
    timestamp = (now or datetime.now(timezone.utc)).strftime("%Y%m%dT%H%M%SZ")
    candidate = target_dir / f"manual-acceptance-packet-{timestamp}.md"
    suffix = 1
    while candidate.exists():
        candidate = target_dir / f"manual-acceptance-packet-{timestamp}-{suffix}.md"
        suffix += 1
    return candidate


