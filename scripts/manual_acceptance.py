    packet.add_argument("--offset", type=int, default=0, help="Incomplete acceptance item offset for packet pagination.")
    packet.add_argument("--reviewer", default=None, help="Optional reviewer name to pre-fill the packet header.")
    packet.add_argument("--pr-url", default=None, help="Optional PR URL to pre-fill the packet header.")
    packet.add_argument("--write-report", action="store_true", help="Write reports/manual-acceptance-packet.md.")
    packet.add_argument("--report-path", default=str(DEFAULT_ACCEPTANCE_PACKET_REPORT), help="Packet path inside the memory root.")
    packet.add_argument("--archive", action="store_true", help="Write a timestamped packet copy under reports/manual-acceptance-packets/.")
    packet.add_argument("--archive-dir", default=str(DEFAULT_ACCEPTANCE_PACKET_ARCHIVE_DIR), help="Archive directory under reports/manual-acceptance-packets/.")

