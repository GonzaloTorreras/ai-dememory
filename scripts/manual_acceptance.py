    template.add_argument("--item", required=True, choices=sorted(ACCEPTANCE_ITEMS))
    template.add_argument("--status", choices=sorted(STATUSES), default="passed")
    template.add_argument("--reviewer", default=None, help="Optional reviewer name to pre-fill the record command.")
    template.add_argument("--pr-url", default=None, help="Optional PR URL to pre-fill the record command artifact.")
    template.add_argument("--json", action="store_true")

    verify = subparsers.add_parser("verify", help="Fail until every manual acceptance item has reviewed passed evidence.")
    verify.add_argument("--json", action="store_true")

    record = subparsers.add_parser("record", help="Record reviewed manual acceptance evidence.")
    record.add_argument("--item", required=True, choices=sorted(ACCEPTANCE_ITEMS))
    record.add_argument("--status", choices=sorted(STATUSES), default="passed")
    record.add_argument("--reviewed-by", required=True)
    record.add_argument("--summary", required=True)
    record.add_argument("--artifact", action="append", default=[])
    record.add_argument("--json", action="store_true")

    args = parser.parse_args(argv)
    root = repo_root(args.root)

    if args.command in {"list", "status"}:
        items = acceptance_status(root)
        if args.json:
            print(json.dumps(status_to_dict(items), indent=2))
        else:
            print_status(items)
        return 0

