def acceptance_dir(root: Path) -> Path:
    return root / "inbox" / "release-acceptance"


def safe_acceptance_dir(root: Path, *, create: bool) -> Path | None:
    root_abs = root.resolve()
    inbox = root_abs / "inbox"
    directory = inbox / "release-acceptance"
    for component in (inbox, directory):
        if component.is_symlink():
            raise ValueError("acceptance evidence path must not contain symlinks")
        if component.exists():
            try:
                component.resolve().relative_to(root_abs)
            except ValueError as exc:
                raise ValueError("acceptance evidence path must stay inside the memory root") from exc
    if not create and not directory.exists():
        return None
    if create:
        directory.mkdir(parents=True, exist_ok=True)
    for component in (inbox, directory):
        if component.is_symlink():
            raise ValueError("acceptance evidence path must not contain symlinks")
        if component.exists():
            try:
                component.resolve().relative_to(root_abs)
            except ValueError as exc:
                raise ValueError("acceptance evidence path must stay inside the memory root") from exc
    return directory


def frontmatter_scalar(value: str) -> str:
    clean = " ".join(str(value).splitlines()).strip().replace('"', "'")
    return json.dumps(clean)


def frontmatter_list(values: list[str]) -> str:
    return "[" + ", ".join(frontmatter_scalar(value) for value in values) + "]"


def validate_item(item: str) -> None:
    if item not in ACCEPTANCE_ITEMS:
        raise ValueError("unknown acceptance item; use `ai-dememory acceptance list`")


def record_acceptance(
    root: Path,
    item: str,
    status: str,
    reviewed_by: str,
    summary: str,
    artifacts: list[str] | None = None,
) -> Path:
    validate_item(item)
    if status not in STATUSES:
        raise ValueError("status must be passed or blocked")
    reviewed_by = reviewed_by.strip()
    summary = summary.strip()
    artifacts = [artifact.strip() for artifact in (artifacts or []) if artifact.strip()]
    if not reviewed_by:
        raise ValueError("reviewed_by is required")
    if not summary:
        raise ValueError("summary is required")

    scan_target = "\n".join([item, status, reviewed_by, summary, *artifacts])
    if scan_text(scan_target, "<manual-acceptance>"):
        raise ValueError("acceptance evidence rejected by secret scan")

    now = datetime.now(timezone.utc).replace(microsecond=0)
    directory = safe_acceptance_dir(root, create=True)
    assert directory is not None
    stem = f"{now.strftime('%Y%m%dT%H%M%SZ')}_{slugify(item, 'acceptance')}"
    path = directory / f"{stem}.md"
    suffix = 1
    while path.exists() or path.is_symlink():
        path = directory / f"{stem}_{suffix}.md"
        suffix += 1
    artifact_lines = "\n".join(f"- {artifact}" for artifact in artifacts) if artifacts else "- none"
    text = f"""---
type: manual-acceptance
status: {status}
acceptance_item: {frontmatter_scalar(item)}
reviewed_by: {frontmatter_scalar(reviewed_by)}
reviewed_at: {now.date().isoformat()}
summary: {frontmatter_scalar(summary)}
artifacts: {frontmatter_list(artifacts)}
---

# Manual Acceptance: {item}

{ACCEPTANCE_ITEMS[item]}

## Summary

{summary}

## Artifacts

{artifact_lines}
"""
    if scan_text(text, "<manual-acceptance.rendered>"):
        raise ValueError("acceptance evidence rejected by rendered secret scan")
    path.write_text(text, encoding="utf-8")
    return path


def load_acceptance_records(root: Path) -> list[AcceptanceRecord]:
    try:
        directory = safe_acceptance_dir(root, create=False)
    except ValueError:
        return []
    if directory is None:
        return []
    records: list[AcceptanceRecord] = []
    for path in sorted(directory.glob("*.md")):
        if path.is_symlink():
            continue
        try:
            path.resolve().relative_to(directory.resolve())
        except ValueError:
            continue
        try:
            frontmatter, _ = parse_frontmatter_text(path.read_text(encoding="utf-8"), path)
        except (OSError, FrontmatterError):
            continue
        record = record_from_frontmatter(root, path, frontmatter)
        if record:
            records.append(record)
    return records


def record_from_frontmatter(root: Path, path: Path, data: dict[str, Any]) -> AcceptanceRecord | None:
    if data.get("type") != "manual-acceptance":
        return None
    item = data.get("acceptance_item")
    status = data.get("status")
    reviewed_by = data.get("reviewed_by")
    reviewed_at = data.get("reviewed_at")
    summary = data.get("summary")
    artifacts = data.get("artifacts", [])
    if item not in ACCEPTANCE_ITEMS or status not in STATUSES:
        return None
    if not isinstance(reviewed_by, str) or not reviewed_by.strip():
        return None
    if not is_date_string(reviewed_at):
        return None
    if not isinstance(summary, str) or not summary.strip():
        return None
    if not isinstance(artifacts, list) or not all(isinstance(artifact, str) for artifact in artifacts):
        artifacts = []
    return AcceptanceRecord(
        path=repo_relative_path(path, root),
        item=str(item),
        status=str(status),
        reviewed_by=reviewed_by,
        reviewed_at=str(reviewed_at),
        summary=summary,
        artifacts=list(artifacts),
    )


def acceptance_status(root: Path) -> list[AcceptanceItemStatus]:
    records = load_acceptance_records(root)
    by_item: dict[str, list[AcceptanceRecord]] = {item: [] for item in ACCEPTANCE_ITEMS}
    for record in records:
        by_item.setdefault(record.item, []).append(record)
    return [
        AcceptanceItemStatus(
            id=item,
            description=description,
            completed=bool(by_item.get(item)) and by_item.get(item, [])[-1].status == "passed",
            records=by_item.get(item, []),
        )
        for item, description in ACCEPTANCE_ITEMS.items()
    ]


def remaining_acceptance_items(root: Path) -> list[str]:
    return [item.description for item in acceptance_status(root) if not item.completed]


def verify_acceptance(items: list[AcceptanceItemStatus]) -> AcceptanceVerification:
    completed: list[dict[str, Any]] = []
    blocked: list[dict[str, Any]] = []
    remaining: list[dict[str, Any]] = []
    for item in items:
        row = {
            "id": item.id,
            "description": item.description,
            "records": [asdict(record) for record in item.records],
        }
        if item.completed:
            completed.append(row)
        else:
            remaining.append(row)
            if any(record.status == "blocked" for record in item.records):
                blocked.append(row)
    return AcceptanceVerification(
        complete=not remaining,
        total=len(items),
        completed=completed,
        blocked=blocked,
        remaining=remaining,
    )


def command_arg(value: str) -> str:
    normalized = " ".join(str(value).splitlines()).strip()
    return "'" + normalized.replace("'", "'\"'\"'") + "'"


def markdown_code_span(value: str | None, default: str = "not provided") -> str:
    text = value if isinstance(value, str) and value else default
    text = re.sub(r"\s+", " ", text).strip() or default
    max_backtick_run = max((len(match.group(0)) for match in re.finditer(r"`+", text)), default=0)
    delimiter = "`" * (max_backtick_run + 1)
    if text.startswith("`") or text.endswith("`"):
        text = f" {text} "
    return f"{delimiter}{text}{delimiter}"


def acceptance_record_command(
    item_id: str,
    status: str = "passed",
    *,
    reviewer: str | None = None,
    pr_url: str | None = None,
) -> str:
    reviewed_by = reviewer.strip() if isinstance(reviewer, str) and reviewer.strip() else "Reviewer Name"
    artifacts = [pr_url.strip()] if isinstance(pr_url, str) and pr_url.strip() else ["<path-or-url>"]
    pieces = [
        "ai-dememory acceptance record",
        f"--item {item_id}",
        f"--reviewed-by {command_arg(reviewed_by)}",
        "--summary 'Reviewed evidence summary.'",
    ]
    if status != "passed":
        pieces.insert(2, f"--status {status}")
    for artifact in artifacts:
        pieces.append(f"--artifact {command_arg(artifact)}")
    return " ".join(pieces)


def resolve_repo_path(root: Path, path: str | Path) -> Path:
    candidate = Path(path)
    if not candidate.is_absolute():
        candidate = root / candidate
    return candidate.resolve()


def render_acceptance_plan_report(plan: AcceptancePlan) -> str:
    lines = [
        "# Manual Acceptance Plan",
        "",
        "This generated report plans manual release acceptance work. It does not",
        "record evidence, complete acceptance, publish packages, or replace human",
        "review.",
        "",
        "## Summary",
        "",
        f"- complete: `{str(plan.complete).lower()}`",
        f"- completed: `{plan.completed_count}/{plan.total}`",
        f"- remaining_count: `{plan.remaining_count}`",
        f"- blocked_count: `{plan.blocked_count}`",
        f"- reviewer: {markdown_code_span(plan.reviewer)}",
        f"- pr_url: {markdown_code_span(plan.pr_url)}",
        "",
    ]

    for section_status, title in [
        ("remaining", "Remaining"),
        ("blocked", "Blocked"),
        ("passed", "Completed"),
    ]:
        section_items = [item for item in plan.items if item.status == section_status]
        lines.extend([f"## {title}", ""])
        if not section_items:
            lines.extend([f"_No {title.lower()} manual acceptance items._", ""])
            continue
        for item in section_items:
            lines.extend(
                [
                    f"### {item.id}",
                    "",
                    f"- status: `{item.status}`",
                    f"- completed: `{str(item.completed).lower()}`",
                    f"- record_count: `{item.record_count}`",
                    f"- description: {item.description}",
                    f"- next_action: {item.next_action}",
                    "",
                    "Suggested Artifacts:",
                    "",
                ]
            )
            lines.extend(f"- {artifact}" for artifact in item.suggested_artifacts)
            lines.append("")
            if item.latest_record:
                latest = item.latest_record
                lines.extend(
                    [
                        "Latest Record:",
                        "",
                        f"- path: `{latest.get('path')}`",
                        f"- status: `{latest.get('status')}`",
                        f"- reviewed_by: `{latest.get('reviewed_by')}`",
                        f"- reviewed_at: `{latest.get('reviewed_at')}`",
                        f"- summary: {latest.get('summary')}",
                        "",
                    ]
                )
            else:
                lines.extend(["Latest Record:", "", "_No reviewed records._", ""])
            if item.pass_command or item.blocked_command:
                lines.extend(["Record Commands:", ""])
                if item.pass_command:
                    lines.extend(["Pass:", "", "```bash", item.pass_command, "```", ""])
                if item.blocked_command:
                    lines.extend(["Block:", "", "```bash", item.blocked_command, "```", ""])

    lines.extend(["## Next Actions", ""])
    lines.extend(f"- {action}" for action in plan.next_actions)
    lines.extend(
        [
            "",
            "## Boundaries",
            "",
            "- This is a generated planning artifact under `reports/`.",
            "- It does not record evidence or count as manual acceptance.",
            "- Run `ai-dememory acceptance record` only after a reviewer has real proof.",
            "- Run `ai-dememory acceptance verify` and `ai-dememory release-evidence --strict` before release.",
            "",
        ]
    )
    return "\n".join(lines)


