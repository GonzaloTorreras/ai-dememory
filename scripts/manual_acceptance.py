def acceptance_packet_archive_generated_at(path: Path) -> str | None:
    match = re.fullmatch(r"manual-acceptance-packet-(\d{8}T\d{6}Z)(?:-\d+)?\.md", path.name)
    if not match:
        return None
    try:
        generated = datetime.strptime(match.group(1), "%Y%m%dT%H%M%SZ").replace(tzinfo=timezone.utc)
    except ValueError:
        return None
    return generated.isoformat().replace("+00:00", "Z")


def acceptance_packet_archive_sort_key(path: Path) -> tuple[bool, str, int, str]:
    match = re.fullmatch(r"manual-acceptance-packet-(\d{8}T\d{6}Z)(?:-(\d+))?\.md", path.name)
    if not match:
        return (False, "", -1, path.name)
    suffix = int(match.group(2) or 0)
    return (True, match.group(1), suffix, path.name)


